/**
 * Get a category by name
 */
export function getCategoryByName(name: string): WordCategory | undefined {
  return WORD_CATEGORIES.find((cat) => cat.name === name);
}

/**
 * Get random words from a category
 */
export function getRandomWordsFromCategory(
  categoryName: string,
  count: number
): string[] {
  const category = getCategoryByName(categoryName);
  if (!category) return [];

  const shuffled = [...category.words].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

/**
 * Get all category names
 */
export function getAllCategoryNames(): string[] {
  return WORD_CATEGORIES.map((cat) => cat.name);
}

/**
 * Get display name for a category
 */
export function getCategoryDisplayName(name: string): string {
  const category = getCategoryByName(name);
  return category?.displayName || name;
}
